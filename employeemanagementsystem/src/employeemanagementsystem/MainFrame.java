    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        DbOperations dpOps = new DbOperations();
        int x = dpOps.checkLogin(txtUserName.getText(), txtPassword.getText());
        if (x == 0) {
            JOptionPane.showMessageDialog(this, "Login Successful..!!");
        } else if (x == 1) {
            JOptionPane.showMessageDialog(this, "Incorrect Password..!!");
        } else if (x == 2) {
            JOptionPane.showMessageDialog(this, "Incorrect username and password..!!");
        } else if (x == 3) {
            JOptionPane.showMessageDialog(this, "Error occured while check login");
        }

    }//GEN-LAST:event_btnLoginActionPerformed

